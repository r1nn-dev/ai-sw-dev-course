-- ============================================================
-- 실행 흐름
-- 1. AUTHOR 테이블을 선택한다.
-- 2. - `LASTNAME`이 S로 시작하는 행만 남긴다.
-- 3. `COUNTRY` 기준으로 그룹화한다.
-- 4. 국가별 행 수를 계산한다.
-- 5. 그룹 결과 중 `COUNT(*) >= 1`인 그룹만 남긴다.
-- 6. `cnt` 기준으로 내림차순 정렬한다.
-- ============================================================


-- 성이 S로 시작하는 저자를 국가별로 묶고, 
-- 국가별 저자 수를 계산한 뒤, 
-- 저자 수 기준 내림차순으로 정렬한다.

SELECT COUNTRY, COUNT(*) AS cnt
FROM AUTHOR
WHERE LASTNAME LIKE 'S%'
GROUP BY COUNTRY
HAVING COUNT(*) >= 1
ORDER BY cnt DESC;
